"""
    Ask for 2 numbers and operate all basic math operations
"""

numberA = int(input("Insert first number:"))
numberB = int(input("Insert second number:"))

# sum
print(f"{numberA} + {numberB} = {numberA + numberB}")
# res
print(f"{numberA} - {numberB} = {numberA - numberB}")
# mult
print(f"{numberA} * {numberB} = {numberA * numberB}")
# div
print(f"{numberA} / {numberB} = {numberA / numberB}")